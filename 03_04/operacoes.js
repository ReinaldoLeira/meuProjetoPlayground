//incremento
let numero = 10
console.log(numero++) //exibe sem incrementar e depois somente incrementa
console.log(numero) //numero já incrementado
console.log(++numero) //exibe depois de incrementar

//decremento
console.log(numero--) //exibe sem decrementar e depois somente decrementa
console.log(numero) //numero já decrementado
console.log(--numero) //exibe depois de decrementar

console.log('-Comparação maior e menor-')
//comparação maior
console.log(10 > 5)

//comparação menor
console.log(10 < 5)
console.log('-Comparação menor ou igual e maior ou igual-')

//comparação menor ou igual
console.log(3 <= 3)

//comparação maior ou igual
console.log(20 >= 20)
console.log('-Comparador de igualdade simples e rigido-')

//comparação de igualdade
console.log(10 == 10)

//comparação de igualdade rigido
console.log(10 === '10')
console.log('-Comparador de diferença simples e rigido-')

//comparação de diferença
console.log(10 != 10)

//comparação de diferença rigido
console.log(10 !== '10')
console.log('-comparador AND, OR e NOT-')

//AND
console.log(10 > 5 && 8 > 10)

//OR
console.log(10 > 5 || 8 > 10)

//NOT
console.log(!5 > 3)
